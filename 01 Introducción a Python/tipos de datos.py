# Tipos de datos en Python
print(type(150)) # int -> integer -> número entero
print(type(35.2)) # float -> punto flotante -> número real
print(type(10j)) # complex -> número complejo
print(type(True)) # bool -> booleano -> Veradero
print(type(False)) # Falso
print(type("Hola")) # str -> string -> Texto
print(type('Hola'))
print(type([10,65.1,"hola",True,[1,2]]))
# list -> Conjuntos ordenados y mutables de datos
print(type((10,65.1,"hola",True,[1,2])))
# tuple -> Conjunto ordenado e inmutable de datos
print(type({"llave": "valor", 123: [1,2,3,5]}))
# dict -> diccionario -> Conjunto ordenado, mutable y nombrado de datos